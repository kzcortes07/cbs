    public ArrayList<User2> userList()
    {
        ArrayList<User2> usersList = new ArrayList<>();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cbs_db","root", "");
            String query1 = "SELECT * FROM equipment_types";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            User2 user;
            while(rs.next())
            {
                user = new User2(rs.getInt("id"), rs.getString("name"), rs.getString("description"));
                usersList.add(user);
            }
                    
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
        return usersList;
    }
    
    public void show_user2()
    {
        ArrayList<User2> list = userList();
        DefaultTableModel model = (DefaultTableModel)item_table.getModel();
        
        Object[] row = new Object[6];
        for (int i = 0; i < list.size(); i++)
        {
            row[0]=list.get(i).getid();
            row[1]=list.get(i).getname();
            row[2]=list.get(i).getdescription();
            model.addRow(row);
         
        }
        
    }
