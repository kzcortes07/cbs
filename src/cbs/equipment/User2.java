class User2 {
    private int id;
    private String name;
    private String description;
    
    public User2(int id, String name, String description)
    {
        this.id = id;
        this.name = name;
        this.description = description;
    }
    
    public int getid()
    {
        return id;
    }
    
    public String getname()
    {
        return name;
    }
    
    public String getdescription()
    {
        return description;
    }
    
    
    
}
